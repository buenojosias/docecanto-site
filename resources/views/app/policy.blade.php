            <h1>POLÍTICA DE PRIVACIDADE</h1>
            <p><small>Data de vigência: 6 de agosto de 2023.</small></p>

            <h2>INTRODUÇÃO</h2>
            <p>Bem-vindo à Política de Privacidade do Coral Doce Canto. Nós nos preocupamos com
            a privacidade dos nossos usuários e estamos empenhados em proteger as informações pessoais que coletamos.
            Esta Política de Privacidade descreve como coletamos, usamos, armazenamos e compartilhamos informações
            quando você utiliza o nosso aplicativo de apoio ao coral infanto-juvenil.</p>

            <h2>INFORMAÇÕES COLETADAS</h2>
            <h3>2.1 Informações fornecidas pelo usuário:</h3>
            <p>Para utilizar o aplicativo, você precisará fornecer informações de login, incluindo um nome de usuário e
            senha gerados externamente pelo administrador do coral. O aplicativo é de uso exclusivo dos integrantes do
            coral e o administrador se reserva o direito de conceder ou revogar o acesso ao aplicativo conforme
            necessário. Em caso de desligamento do integrante, sua conta será excluída pelo administrador.</p>

            <h3>2.2 Informações de uso:</h3>
            <p>Coletamos automaticamente informações sobre o seu uso do aplicativo, incluindo as músicas acessadas, áudios
            reproduzidos, datas e horários de acesso e outras informações de interação com o aplicativo.</p>

            <h2>USO DAS INFORMAÇÕES</h2>
            <h3>3.1 Fornecimento do serviço:</h3>
            <p>Utilizamos as informações coletadas para fornecer o serviço do aplicativo, incluindo a disponibilização de
            letras e áudios de músicas, a agenda do coral e a lista de aniversariantes do mês.</p>

            <h3>3.2 Comunicações:</h3>
            <p>Podemos utilizar o seu endereço de e-mail para enviar comunicações relacionadas ao funcionamento do
            aplicativo, como atualizações, novidades ou alterações nos termos de serviço ou na política de privacidade.</p>

            <h3>3.3 Melhorias no aplicativo:</h3>
            <p>As informações coletadas também podem ser utilizadas para aprimorar o funcionamento do aplicativo,
            garantindo uma experiência cada vez mais personalizada e eficiente para os usuários.</p>

            <h2>COMPARTILHAMENTO DAS INFORMAÇÕES</h2>
            <h3>4.1 Com terceiros:</h3>
            <p>Não compartilhamos suas informações pessoais com terceiros, a menos que haja consentimento expresso do
            usuário ou por exigência legal.</p>

            <h3>4.2 Com outros usuários do aplicativo:</h3>
            <p>Alguns recursos do aplicativo podem permitir que os usuários compartilhem informações entre si, como a lista
            de aniversariantes do mês. O compartilhamento de informações nesses casos é opcional e fica a critério do
            usuário.</p>

            <h2>SEGURANÇA DOS DADOS</h2>
            <p>Empregamos medidas de segurança para proteger as informações coletadas contra acesso não autorizado,
            alteração, divulgação ou destruição não autorizada. No entanto, nenhum método de transmissão pela internet
            ou método de armazenamento eletrônico é 100% seguro, portanto, não podemos garantir a segurança absoluta dos
            dados.</p>

            <h2>DIREITOS DOS USUÁRIOS</h2>
            <p>Você tem direito a acessar, corrigir, atualizar ou excluir suas informações pessoais fornecidas por meio do
            aplicativo. Se desejar exercer esses direitos ou tiver alguma dúvida sobre nossas práticas de privacidade,
            entre em contato conosco por meio do endereço de e-mail fornecido no final desta política.</p>

            <h2>ALTERAÇÕES NA POLÍTICA DE PRIVACIDADE</h2>
            <p>Esta Política de Privacidade pode ser atualizada periodicamente para refletir mudanças nas práticas de
            coleta e uso de informações. Notificaremos os usuários sobre alterações significativas por meio de um aviso
            no aplicativo ou por e-mail, se aplicável.</p>

            <h2>CONTATO</h2>
            <p>Se você tiver dúvidas ou preocupações sobre esta Política de Privacidade ou sobre o aplicativo, entre em
            contato conosco através do seguinte endereço de e-mail: coraldocecanto@gmail.com.</p>

